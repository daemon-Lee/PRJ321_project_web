package model;

import java.util.Date;

/**
 *
 * @author Tuan Anh
 */
public class User {
    int id;
    String name,email,detail;
    Date birthDay;

    public User() {
    }

    public User(int id, String name, String email, String detail, Date birthDay) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.detail = detail;
        this.birthDay = birthDay;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public Date getBirthDay() {
        return birthDay;
    }

    public void setBirthDay(Date birthDay) {
        this.birthDay = birthDay;
    }
    
}
